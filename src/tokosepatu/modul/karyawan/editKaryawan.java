    private void selectJabatan() {
        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT id, bagian FROM tb_gaji_karyawan");
            while (res.next()) {
                cmboxJabatan.addItem(res.getString("bagian"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

