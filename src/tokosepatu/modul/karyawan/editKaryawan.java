    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int idkaryawan = Integer.parseInt(txtId.getText());
        String namakaryawan = txtNama.getText();
        String noTelpon = txtTelpon.getText();
        String jk = cmboxjk.getSelectedItem().toString();
        String Alamat = txtAlamat.getText();
        
        
        int idJabatan = 0;
        String selectedJabatan = (String) cmboxJabatan.getSelectedItem();
        
        if (null != selectedJabatan) switch (selectedJabatan) {
            case "Manager":
                idJabatan = 1;
                break;
            case "Kasir":
                idJabatan = 2;
                break;
            case "Costumer Service":
                idJabatan = 3;
                break;
            case "Gudang":
                idJabatan = 4;
                break;
            default:
                break;
        }

        try {
            String sql = "UPDATE tb_karyawan SET "
                    + "nama = '" + namakaryawan + "'"
                    + ", alamat = '" + Alamat + "'"
                    + ", telepon = '" + noTelpon + "'"
                    + ", jenis_kelamin = '" + jk + "'"
                    + ", jabatan = " + idJabatan
                    + " WHERE id = " + idkaryawan;
            
            java.sql.Connection conn;
            conn = (Connection) Koneksi.getConnection();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil");
            Karyawan kry = new Karyawan();
            kry.setVisible(true);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }   
    }//GEN-LAST:event_btnEditActionPerformed

