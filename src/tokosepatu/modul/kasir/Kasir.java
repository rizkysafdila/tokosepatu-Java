    private void btnBillingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBillingActionPerformed
        // TODO add your handling code here:
        int numRow = cartTable.getRowCount();
        TableModel model = cartTable.getModel();
        String uniqueID = UUID.randomUUID().toString();

        String bayar = metodeBayarSelect.getSelectedItem().toString();
        int metode = 0;

        if (bayar == "Cash") {
            metode = 1;
        } else if (bayar == "Dana") {
            metode = 2;
        } else if (bayar == "ShopeePay") {
            metode = 3;
        } else if (bayar == "Link Aja") {
            metode = 4;
        } else if (bayar == "Gopay") {
            metode = 5;
        }

        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO tb_transaksi(id, id_barang, qty, total_bayar, metode_pembayaran) VALUES(?,?,?,?,?)");
            for (int i = 0; i < numRow; i++) {
                String[] produk = model.getValueAt(i, 0).toString().split(" - ");
                String idProduk = produk[0].toLowerCase();

                int qty = Integer.parseInt(model.getValueAt(i, 1).toString());
                double harga = Double.parseDouble(model.getValueAt(i, 2).toString());

                stmt.setString(1, uniqueID);
                stmt.setString(2, idProduk);
                stmt.setInt(3, qty);
                stmt.setDouble(4, harga);
                stmt.setInt(5, metode);

                stmt.executeUpdate();
            }
            FormBayar pay = new FormBayar();
            pay.totalBayar.setText(totalBayarField.getText());
            pay.idTransaksiField.setText(uniqueID);
            pay.setVisible(true);
            pay.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_btnBillingActionPerformed

