    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dataModel = (DefaultTableModel) cartTable.getModel();
        List list = new ArrayList<>();
        cartTable.setAutoCreateColumnsFromModel(true);

        String[] value = produkSelect.getSelectedItem().toString().split(" - ");
        String produk = value[0] + " - " + value[1];

        Double harga = Double.parseDouble(value[2]);

        int qty = Integer.parseInt(qtyField.getText());
        Double hargaJual = harga * qty;

        list.add(produk);
        list.add(qty);
        list.add(hargaJual);
        dataModel.addRow(list.toArray());

        countTotal();
    }//GEN-LAST:event_btnAddItemActionPerformed

