    private void selectMerk() {
        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM tb_merk");
            while (res.next()) {
                jComboBoxMerk.addItem(res.getInt("id") + " - " + res.getString("merk"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }
    
    private void selectKategori() {
        try {
            Statement statement = (Statement) Koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM tb_kategori");
            while (res.next()) {
                jComboBoxKategori.addItem(res.getInt("id") + " - " + res.getString("kategori"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

