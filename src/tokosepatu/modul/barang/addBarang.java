    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = (PreparedStatement) conn.prepareStatement("insert into tb_barang(id, nama_barang, id_merk, id_kategori, ukuran, harga_beli, harga_jual, keterangan) values(?,?,?,?,?,?,?,?)");

            stmt.setString(1, txtid.getText());
            stmt.setString(2, txtNamabarang.getText());

            String[] merk = jComboBoxMerk.getSelectedItem().toString().split(" - ");
            int idMerk = Integer.parseInt(merk[0]);
            stmt.setInt(3, idMerk);
            
            String[] kategori = jComboBoxKategori.getSelectedItem().toString().split(" - ");
            int idKategori = Integer.parseInt(kategori[0]);
            stmt.setInt(4, idKategori);
            
            int ukuran = Integer.parseInt(txtukuran.getText());
            stmt.setInt(5, ukuran);

            double harga_beli = Double.parseDouble(txtHargabeli.getText());
            stmt.setDouble(6, harga_beli);
            
            double harga_jual = Double.parseDouble(txtHargajual.getText());
            stmt.setDouble(7, harga_jual);
            
            stmt.setString(8, txtKeterangan.getText());

            stmt.executeUpdate();
            setVisible(false);
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            stokbarang brg = new stokbarang();
            brg.setVisible(true);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

